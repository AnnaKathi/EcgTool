//---------------------------------------------------------------------------
#pragma hdrstop

#include "classEcg.h"
//---------------------------------------------------------------------------
#pragma package(smart_init)
//---------------------------------------------------------------------------
cEcg::cEcg()
	: frpeaks(new cRpeaks), fqrs(new cQrs), fheart(new cHeartbeats), fdata(new cData)
	{
	}
//---------------------------------------------------------------------------
cEcg::~cEcg()
	{
	if (fdata)  delete fdata;
	if (fqrs)   delete fqrs;
	if (fheart) delete fheart;
	}
//---------------------------------------------------------------------------
cData& cEcg::get_data()
	{
	return *fdata;
	}
//---------------------------------------------------------------------------
cRpeaks& cEcg::get_rpeaks()
	{
	return *frpeaks;
	}
//---------------------------------------------------------------------------
cTurns& cEcg::get_turns()
	{
	return *fturns;
	}
//---------------------------------------------------------------------------
cQrs& cEcg::get_qrs()
	{
	return *fqrs;
	}
//---------------------------------------------------------------------------
cHeartbeats& cEcg::get_heart()
	{
	return *fheart;
	}
//---------------------------------------------------------------------------
